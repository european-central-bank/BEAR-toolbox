%[text] ## Quick start
%[text] ### New Estimation
%[text] Go to an empty directory
mkdir('NewEstimationFolder')
cd('NewEstimationFolder')
%[text] Start a new estimation:
gui.start
%%
%[text] ### Existing Estimation
%[text] If you already had a BEAR estimation done, you can run:
cd('NewEstimationFolder')
gui.resume


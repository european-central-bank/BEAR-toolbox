%[text] # BEARX Toolbox
%[text] **Note:** For BEAR5 documents, please refer to [BEAR5 - Getting Started](file:.\GettingStartedBear5.mlx).
%[text] ## Quick start
%[text] Run the BEAR6 app via the toolstrip button or directly running:
BEAR6
%%
%[text] Alternatively run a local estimation with:
gui.start
%%
%[text] or resume a previous one in this folder
gui.resume
%%
%[text] After running the UI once, a `master.m` file will be created such that you can run the model again without the UI.

